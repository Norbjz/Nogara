console.log(num1 != num2); //true


// Mayor o igual, menor o igual: >=, <=

var edadCliente = 18;
var minimoEdad = 18

if (edadCliente >= minimoEdad) {
    console.log('Pásale brodi');
}


// Igualdad estricta: ===

var num3 = 10;
var num4 = '10';

console.log(num3 == num4); //true
console.log(num3 === num4); //false

var cero = 0;
var falso = false;

console.log(cero == falso); //true
